package com.apps.afis.crismasantuario.Models;

import java.util.Date;

public class Crismando {

    private String idCrismando;
    private String nomeCrismando;
    private String telefoneCrismando;
    private Date nascimentoCrismando;
    private String nomePaiRspCrismando;
    private String telPaiRspCrismando;

    public Crismando() {
    }

    public String getIdCrismando() {
        return idCrismando;
    }

    public void setIdCrismando(String idCrismando) {
        this.idCrismando = idCrismando;
    }

    public String getNomeCrismando() {
        return nomeCrismando;
    }

    public void setNomeCrismando(String nomeCrismando) {
        this.nomeCrismando = nomeCrismando;
    }

    public String getTelefoneCrismando() {
        return telefoneCrismando;
    }

    public void setTelefoneCrismando(String telefoneCrismando) {
        this.telefoneCrismando = telefoneCrismando;
    }

    public Date getNascimentoCrismando() {
        return nascimentoCrismando;
    }

    public void setNascimentoCrismando(Date nascimentoCrismando) {
        this.nascimentoCrismando = nascimentoCrismando;
    }

    public String getNomePaiRspCrismando() {
        return nomePaiRspCrismando;
    }

    public void setNomePaiRspCrismando(String nomePaiRspCrismando) {
        this.nomePaiRspCrismando = nomePaiRspCrismando;
    }

    public String getTelPaiRspCrismando() {
        return telPaiRspCrismando;
    }

    public void setTelPaiRspCrismando(String telPaiRspCrismando) {
        this.telPaiRspCrismando = telPaiRspCrismando;
    }

}
